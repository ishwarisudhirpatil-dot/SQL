CREATE DATABASE SALESDB;
USE SALESDB;
CREATE TABLE Sales (
  sale_id INT AUTO_INCREMENT PRIMARY KEY,
  product_name VARCHAR(100),
  quantity INT,
  price DECIMAL(10,2),
  sale_date DATE
);
